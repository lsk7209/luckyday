// Custom matchers
expect.extend({
  toHaveBeenCalledWithMatch(received, ...expected) {
    const pass = expected.some(expectedArgs =>
      this.equals(received, expect.objectContaining(expectedArgs))
    )

    if (pass) {
      return {
        message: () =>
          `expected ${received} not to match any of the expected arguments`,
        pass: true,
      }
    } else {
      return {
        message: () =>
          `expected ${received} to match one of the expected arguments`,
        pass: false,
      }
    }
  },
})
